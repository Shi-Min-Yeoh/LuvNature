$(document).ready(function() {
    $('#tapir').click(function() {
        const audio = document.getElementsByTagName("audio")[0];
        const icon = document.getElementById("tapir");

        if (audio.paused) {
            audio.play();
            icon.style.opacity = "1";
        } else {
            audio.pause();
            icon.style.opacity = ".5";
        }
        
        return false;
    });
});

